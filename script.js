var array = [
    {
        name: 'Gagan'
    },
    {
        name: 'Bist'
    }
]

//********   For Each function TO PUSH VALUE IN NEW ARRAY  ******** */

var newArray = [];

array.forEach(res => {
    this.newArray.push(res.name)
});

console.log(' Old Array ', array);
console.log(' New Array ', this.newArray);